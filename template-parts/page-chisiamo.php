        <h2>La nostra storia</h2>

        <div class="storia-div">
            <span class="storia-paragraph">
                <p>
                    L’Associazione ERO FORESTIERO affonda le sue radici, solidamente piantate ormai da anni, nel territorio di Vigevano attraverso l’operato inesauribile e costante dei propri volontari.<br>
                    Nel lontano 1996, la fondatrice Agnese Magnani ebbe l’idea di sviluppare un progetto di aiuto concreto nei confronti di chi ha deciso di partire da lontano per venire a vivere in Italia.<br> 
                    Agnese fondò questa Associazione in maniera conforme al suo carattere.<br>
                    Era schietta, coraggiosa e solitaria e si rese subito conto che occuparsi di immigrazione era una necessità e un lavoro delicato, da portare avanti con un’idea equilibrata rispetto alle difficoltà ed alle soddisfazioni che questo poteva portare con sé. <br>
                    Viva in lei l’urgenza di intervenire, fornendo dei servizi che mancavano sul territorio, nella consapevolezza che l’immigrazione non sia di per sé categorizzabile come un bene o un male assoluto ma che sia anche un fatto ineluttabile e che dunque ci si debba offrire come volontari affinché sia una risorsa per la società. <br>
                    Pur essendo Ero Forestiero una sua creatura, aveva compreso che il suo impegno avrebbe prodotto frutti maturi solo se fosse riuscita ad infondere la sua stessa passione in volontari che avrebbero potuto portare l’Associazione a maturare anche quando lei se ne fosse andata. <br>
                    Così è stato. Ero Forestiero vive oggi oltre Agnese ma con Agnese nel cuore, proprio come abbiamo voluto prometterle quando le forze iniziarono a mancarle. <br>
                    Ero Forestiero offre, grazie a Lei, alfabetizzazione per stranieri con un taglio umano e dà la possibilità ai suoi Volontari di crescere al suo interno con lo stesso spirito. <br>
                    Con questo stesso spirito portiamo avanti ancora oggi le nostre attività. <br>
                    Era il 1996! <br>
                    Se percorriamo a ritroso il filo della memoria ritroviamo una Vigevano differente da quella di oggi, come diverse erano le condizioni socio-economiche del nostro Paese e i problemi con cui si confrontava, come diversi eravamo noi. <br>
                    Simile ad allora è però il modo in cui ci rapportiamo a chi è comunemente definito straniero ed è di questo che Ero Forestiero si occupa grazie all’opera dei suoi volontari.
                </p>
